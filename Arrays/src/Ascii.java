import java.util.*;
public class Ascii {

	public static void main(String[] args) {
				char ch;
				System.out.println("Lower case Alphabets are: \n");
				for(ch=97; ch<=122; ch++){
				System.out.print(ch+" ");
				
				}
				System.out.print("\n");
				System.out.println("Upper case Alphabets are: \n");
				for(ch=65; ch<=90; ch++){
				System.out.print(ch+" ");
				}
	}

}